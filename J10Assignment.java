public class J10Assignment {
    public static void main(String []args)
    {
        int a;
        a=10;
        System.out.println(a);
        a+=10;//a=a+10
        System.out.println(a);
        a-=10;//a=a-10
        System.out.println(a);
        a*=10;//a=a*10
        System.out.println(a);
        a/=10;//a=a/10
        System.out.println(a);
        a%=10;//a=a%10
        System.out.println(a);

    }
    
}
